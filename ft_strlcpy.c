#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	size;

	if (!dst)
		return (0);
	i = 0;
	size = 0;
	while (src[size])
		size++;
	if (dstsize == 0)
		return (size);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (size);
}
